function [val, dval_dx, dval_dp] = basicInterpolate( p, x );
x2 = x*x;
x3 = x2*x;
x4 = x3*x;
val = (   -0.5*x +     x2 - 0.5*x3)  * p(1) + ...
      ( 1        - 2.5*x2 + 1.5*x3 ) * p(2) + ...
      (     0.5*x +  2*x2 - 1.5*x3)  * p(3) + ...
      (           -0.5*x2 + 0.5*x3)  * p(4);
  
if nargout > 1
    dval_dx = (   -0.5 +    2*x - 1.5*x2)  * p(1) + ...
              (          -  5*x + 4.5*x2 ) * p(2) + ...
              (     0.5 +   4*x - 4.5*x2)  * p(3) + ...
              (            -1*x + 1.5*x2)  * p(4);
end

if nargout > 2
    dval_dp = [...
        (   -0.5*x +     x2 - 0.5*x3),...
        ( 1        - 2.5*x2 + 1.5*x3 ),...
        (     0.5*x +  2*x2 - 1.5*x3),...
        (           -0.5*x2 + 0.5*x3) ];
end